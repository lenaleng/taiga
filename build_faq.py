current_q = None
current_a = []

def flush_q():
    global html, current_q, current_a
    if current_q:
        html += '                    <div class="faq-item">\n'
        html += f'                        <button class="faq-question">\n                            {current_q}\n                            <span class="faq-icon">+</span>\n                        </button>\n'
        html += '                        <div class="faq-answer">\n                            <div class="faq-answer-content">\n'
        
        a_html = ""
        in_list = False
        for line in current_a:
            line = line.strip()
            if not line:
                a_html += "                                <br>\n"
                continue
                
            if line.startswith('- '):
                if not in_list:
                    a_html += "                                <ul>\n"
                    in_list = True
                
                content = line[2:]
                parts = content.split('**')
                parsed = ""
                for i, p in enumerate(parts):
                    if i % 2 == 1: parsed += f"<strong>{p}</strong>"
                    else: parsed += p
                    
                a_html += f"                                    <li>{parsed}</li>\n"
            else:
                if in_list:
                    a_html += "                                </ul>\n"
                    in_list = False
                
                parts = line.split('**')
                parsed = ""
                for i, p in enumerate(parts):
                    if i % 2 == 1: parsed += f"<strong>{p}</strong>"
                    else: parsed += p
                
                a_html += f"                                <p>{parsed}</p>\n"
                
        if in_list:
            a_html += "                                </ul>\n"
            
        html += a_html
        html += '                            </div>\n                        </div>\n                    </div>\n\n'
    current_q = None
    current_a = []

for line in lines:
    line = line.strip()
    if line == '---':
        continue
        
    if line.startswith('### '):
        flush_q()
        html += f'                    <h3 class="faq-category">{line[4:]}</h3>\n\n'
    elif line.startswith('**') and line.endswith('**') and '?' in line:
        flush_q()
        current_q = line.strip('*')
    else:
        # Check if line is a question we forgot bold tags on
        if '?' in line and len(line) < 100 and line.endswith('?'):
             if not current_q:
                 flush_q()
                 current_q = line.strip('*')
                 continue
        current_a.append(line)

flush_q()
html += '                </div>'

